package EvenOrOddNumber;

public class Main {
    public static void main(String[] args) {
        int num = 50;

        if ( num % 2 == 0 ) {
            System.out.print(num);
            System.out.println(" is even.");
        } else {
            System.out.print(num);
            System.out.println(" is odd.");
        }

    }
}
